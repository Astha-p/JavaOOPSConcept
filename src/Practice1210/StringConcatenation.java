package Practice1210;

public class StringConcatenation {

	public static void main(String[] args) {
		
		String s1 = "Good";
		String s2 = "Vibes";
		String s3 = "Only";
		
		int i1 = 21;
		int i2 = 11;
		
		System.out.println(i1+i2);
		System.out.println(i1+s1);
		System.out.println(s1+s2+s3+i1+i2);
		System.out.println(s1+s2+s3+(i1+i2));
		
		
	}

}
